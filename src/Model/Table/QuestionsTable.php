    public function findQuestionById(Query $query, array $options)
    {
        return $query
            ->contain(['Users', 'Tags'])
            ->where(['id' => $options['id']]);
    }

    public function getById($id = null)
    {
        return $this->get($id, ['contain' => ['Users', 'Tags']]);
    }

    
}
